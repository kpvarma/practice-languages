# Include
a = [1, 2, 3]
a.include?(2)  #=> true

# Count
a = [1, 1, 2, 3, 5, 6, 8]
a.count(1) # => 2


# Index
a = [1, 2, 3]
a.index(2) # => 1


# Zip
a = [1, 2, 3]
b = ["x", "y", "z"]
c = a.zip(b) # => [[1, "x"], [2, "y"], [3, "z"]]


